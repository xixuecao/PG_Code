% --- Executes on button press in pushbutton2.
function pushbutton2_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton2 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global n
global point

point=150;
str=handles.myData;
a=str2double(get(handles.edit1, 'String'));
b=str2double(get(handles.edit5, 'String'));
L=str2double(get(handles.edit2, 'String'));
bochang=str2double(get(handles.edit3, 'String'));
N=str2double(get(handles.edit4, 'String'));


value=get(handles.popupmenu1, 'value');
switch value
   
    case 1
        F=a^2/bochang/L*1000000;
       n=0;
        set(handles.edit7,'string',F);
         switch str
             case '手动迭代'
                tiaoxing(bochang,N,a,L,handles);
               
             case '自动迭代至稳定'
                tiaoxing2(bochang,a,L,handles);
                msgbox(['迭代至稳定次数=',num2str(n-1)]);
         end
    case 2 
        F=min(a,b)^2/bochang/L*1000000;
        n=0;
         set(handles.edit7,'string',F);
         switch str
             case '手动迭代'
                 juxing(bochang,a,b,L,N,handles);
             case '自动迭代至稳定' 
                 juxing2(bochang,a,b,L,handles);
                 msgbox(['迭代至稳定次数=',num2str(n-1)])
         end
    case 3
        F=a^2/bochang/L*1000000;
        n=0;
         set(handles.edit7,'string',F);
         switch str
            case '手动迭代'
                   circle(a,L,bochang,N,handles);
            case '自动迭代至稳定'
                   circle2(a,L,bochang,handles);  
                   msgbox(['迭代至稳定次数=',num2str(n-1)]) 
         end
end
        


